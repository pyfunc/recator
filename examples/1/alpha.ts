// Example 1: duplicate block (escapeHtml)
export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

export function demoAlpha(input: string) {
  return escapeHtml(input);
}
